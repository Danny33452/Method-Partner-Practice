public class StringPractice {
    public static void main(String[] args) {
        String gerald = "Hi Gerald";
        System.out.println(gerald.substring(1));
        System.out.println(gerald.substring(1, 7));
        System.out.println(gerald.substring(1, 1));
        System.out.println(gerald.substring(9));
        System.out.println(gerald.indexOf('G',5));
    }
}